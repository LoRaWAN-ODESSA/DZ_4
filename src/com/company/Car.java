package com.company;

public class Car {

    String name;
    String model;
    int year;
    String color;
    double price;
    String regNumber;

    public Car(String name, String model, int year, String color, double price, String regNumber) {
        this.name = name;
        this.model = model;
        this.year = year;
        this.color = color;
        this.price = price;
        this.regNumber = regNumber;
    }
}
